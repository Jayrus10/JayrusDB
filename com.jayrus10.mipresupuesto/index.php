<?php
    // Inicializamos variables para almacenar los valores de los campos
    $nombreArchivo = "";
    $contenidoArchivo = "";

    // Verificamos si se ha enviado el formulario
    if ($_SERVER["REQUEST_METHOD"] == "POST") {
        // Asignamos los valores de los campos a nuestras variables
        $nombreArchivo = $_POST["nombreArchivo"];
        $contenidoArchivo = $_POST["contenidoArchivo"];

        // Creamos el archivo en la carpeta "PRUEBA"
        $rutaArchivo = "PRUEBA/".$nombreArchivo;
        $archivo = fopen($rutaArchivo, "w");
        fwrite($archivo, $contenidoArchivo);
        fclose($archivo);

        // Mostramos un mensaje de confirmación
        echo "Archivo creado con éxito!";
    }
?>

<!-- Formulario para recopilar los datos -->
<form action="<?php echo htmlspecialchars($_SERVER["PHP_SELF"]); ?>" method="post">
    <label for="nombreArchivo">Nombre del archivo:</label>
    <input type="text" id="nombreArchivo" name="nombreArchivo"><br><br>
    <label for="contenidoArchivo">Contenido del archivo:</label>
    <textarea id="contenidoArchivo" name="contenidoArchivo"></textarea><br><br>
    <input type="submit" value="Aceptar">
</form>
